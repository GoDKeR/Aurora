    SPtr<Window> Service::Initialise(CStr Title, UInt Width, UInt Height)
    {
        SPtr<GLFWWindow> Window = eastl::make_shared<GLFWWindow>(GetContext());

        if (Window->Create(Title, Width, Height))
        {
            return Window;
        }
        return nullptr;
    }

